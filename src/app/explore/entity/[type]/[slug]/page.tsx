export const dynamicParams = false;

export function generateStaticParams() {
  return getStandaloneEntities().map((e) => {
    const sep = e.id.indexOf(":");
    return { type: e.id.slice(0, sep), slug: e.id.slice(sep + 1) };
  });
}

export async function generateMetadata({
  params,
}: PageProps<"/explore/entity/[type]/[slug]">): Promise<Metadata> {
  const { type, slug } = await params;
  // Metadata is generated by the platform's universal metadata system from the
  // resolved entity runtime — one source for every facet.
  const rt = engine.entity.resolveByTypeSlug(type, slug);
  if (!rt) return {};
  const meta = engine.metadata.for(rt);
  return buildMetadata({
    title: meta.seo.title,
    description: meta.seo.description,
    path: rt.canonicalPath,
  });
}

export default async function GraphEntityPage({
  params,
}: PageProps<"/explore/entity/[type]/[slug]">) {
  const { type, slug } = await params;
  // Everything flows through the Scientific Data Engine — no manual assembly.
  const resolved = engine.entity.resolveByTypeSlug(type, slug);
  // Entities with their own content entry are served there, not here.
  if (!resolved || resolved.entries.length > 0) notFound();

  const typeLabel = resolved.typeLabel;
  const url = resolved.canonicalPath;
  const related = resolved.related;
  const siblings = engine.recommendation.sameType(resolved.id);
  const hasConnections = resolved.connections.length > 0;
  const topic = resolved.topics[0];

  const crumbs: Crumb[] = [
    { name: "Home", url: "/" },
    { name: "Explore", url: ROUTES.explore },
    { name: resolved.name, url },
  ];

  return (
    <>
      <JsonLd
        data={[
          breadcrumbSchema(crumbs),
          collectionPageSchema({
            name: resolved.name,
            description: resolved.description ?? resolved.name,
            url,
          }),
        ]}
      />

      <Container className="pt-8">
        <Breadcrumbs crumbs={crumbs} />
      </Container>

      <HeroSection
        compact
        accent={resolved.domain === "culture" ? "ember" : "nebula"}
        eyebrow={<span>{typeLabel}</span>}
        title={resolved.name}
        lead={resolved.description}
      >
        <div className="mt-4">
          <Badge tone="accent">Knowledge graph</Badge>
        </div>
      </HeroSection>

      <Container className="mt-8 mb-12 space-y-12">
        <EntityImagery entityId={resolved.id} />

        <div className="grid gap-6 lg:grid-cols-2">
          <EntityDataPanel resolved={resolved} />
          <EntityQualityPanel resolved={resolved} />
        </div>

        <div className="mt-6">
          <EntityProvenancePanel entityId={resolved.id} />
        </div>

        {hasConnections && (
          <section aria-labelledby="connections-heading">
            <h2 id="connections-heading" className="font-display text-xl font-semibold text-fg">
              Connections
            </h2>
            <p className="mt-1 text-sm text-faint">
              How {resolved.name} connects across Asteria Star — scientific,
              cultural, and astrological links are kept separate.
            </p>
            <div className="mt-4">
              <GraphConnections entityId={resolved.id} />
            </div>
          </section>
        )}

        {related.length > 0 && (
          <section aria-labelledby="related-heading">
            <h2 id="related-heading" className="font-display text-xl font-semibold text-fg">
              Related entities
            </h2>
            <ul className="mt-4 grid grid-cols-1 gap-4 sm:grid-cols-2 lg:grid-cols-3">
              {related.map((e) => (
                <li key={e.id} className="contents">
                  <EntityCard entity={e} />
                </li>
              ))}
            </ul>
          </section>
        )}

        {siblings.length > 0 && (
          <section aria-labelledby="siblings-heading">
            <div className="flex items-center justify-between gap-3">
              <h2 id="siblings-heading" className="font-display text-xl font-semibold text-fg">
                More {typeLabel.toLowerCase()}s
              </h2>
              {topic && (
                <a href={topicPath(topic.slug)} className="text-sm text-muted transition hover:text-fg">
                  Browse all →
                </a>
              )}
            </div>
            <ul className="mt-4 grid grid-cols-1 gap-4 sm:grid-cols-2 lg:grid-cols-3">
              {siblings.map((e) => (
                <li key={e.id} className="contents">
                  <EntityCard entity={e} />
                </li>
              ))}
            </ul>
          </section>
        )}

        <EntityRecommendations entityId={resolved.id} />

        {resolved.sources.length > 0 && (
          <SourceList keys={resolved.sources.map((s) => s.key)} />
        )}
      </Container>
    </>
  );
}
